    <p><h1>Wiadomości z Polskiej piłki wodnej</h1></p>
    <h2>Piąta kolejka Ekstraklasy dla Bytomia!</h2><p class="indent">W grudniowy weekend (19-20) nasi seniorzy rozegrali kolejną kolejkę Ekstraklasy w piłce wodnej. Tym razem nasza drużyna zmierzyła się BOX Logistics Waterpolo Poznań.  
W sobotnim, wieczornym meczu nasi zawodnicy okazali się znacznie lepsi, łatwo wygrywając mecz 11-3. Zawodnikiem tego meczu został grający z numerem 4- Jan Radwański. 
W niedzielę rano drużyna z Poznania stawiła znacznie większy opór, doprowadzając do bardzo ciężkiego i wyrównanego meczu. Jednak i tym razem lepsi okazali się Bytomianie ostatecznie wygrywając mecz 12-11. Tym razem MVP dostał Jakub Struzik. 
Skład drużyny WTS Polonia Bytom, wraz z łączną ilością strzelonych bramek. 
