    <h2>MISTRZOSTWA POLSKI MŁODZIEŻOWCA U23</h2>
    <p class="indent">W ubiegły weekend (28-29.11.2020) po raz pierwszy w tym sezonie odbyły się rozgrywki Młodzieżowca- U-23.  

W sobotę swój pierwszy mecz rozegrali zawodnicy WTS Polonii Bytom przeciwko AZS Uniwersytet Warszawski. 

Zawodnicy bytomskiego klubu w tym meczu odnieśli porażkę 10-16 (0-1,1-5, 5-4, 4-6) 

Pierwsza, niezwykle wyrównana kwarta skończyła się jednobramkowym wynikiem na korzyść Warszawy. W drugiej kwarcie Warszawiacy przeszli do ofensywy wygrywając ją 5-1. Bytomianie po zmianie połów zaczęli nadrabiać straty, jednak Warszawiacy nie pozostawali dłużni i ostatecznie kwarta zakończyła się wynikiem 5-4 dla Bytomia. Ostatnia kwarta przyniosła wiele emocji, obydwie drużyny walczyły zaciekle by wygrać cały mecz. Ponownie lepsza okazała się Warszawa, której Bytomianie ulegli 4-6. Za ten mecz w drużynie Bytomia MVP zdobył grający z numerem 2 Mateusz Bomba. 

</p><img class="img1" src="images/aktualnosci2.jpg" alt="Faul"><p class="indent">W niedzielę drużyna prowadzona przez Krzysztofa Paczynę zmierzyła się z Uczniowskim Klubem Sportowym Neptun Łódź. Tym razem Bytomianie okazali się bezprecedensowo lepsi pokonując Łodzian 22-12 (8-2, 4-2, 6-3, 4-5) 

W pierwszej kwarcie nasi zawodnicy byki niezwykle skuteczni w ataku co przyniosło swój efekt w postaci 8 strzelonych bramek i łatwym wygraniu tej kwarty 8-2. 

W następnej kwarcie ofensywa Bytomian nie ustawała, a sama kwarta zakończyła się wygraną 4-2. 

Po zmianie połów drużyna z Bytomia w dalszym ciągu utrzymywała skuteczność w ataku wygrywając trzecią kwartę 6-3.  

Ostatnią kwartę nasi zawodnicy minimalnie przegrali wynikiem 4-5 

Zawodnikiem meczu został Patryk Cebo grający z numerem 11. 

</p><p class="indent">Po tej rundzie Mistrzostw Polski Młodzieżowca Bytomianie uplasowali się na drugim miejscu w grupie, co daje im awans do rundy finałowej. 

