    <p><h1 id="header">Kim jesteśmy?</h1></p>
    <p class="indent">Bytomski klub piłki wodnej – Waterpolowe Towarzystwo Sportowe, powstały w 1992 roku, jest spadkobiercą tradycji sekcji piłki wodnej KS „Polonia” Bytom.
Wspomnianą sekcję utworzyli w 1945 roku przybyli do Bytomia zawodnicy lwowskiego klubu „Pogoń” oraz sportowcy działającego, w naszym mieście, w latach 1920-1945 SVB „Poseidon” 1920. Do tych pierwszych należeli: Zbigniew Papees, Piotr Zemyr, Wojciech Krzyżanowski, Jerzy Gadzikiewicz , natomiast najwybitniejszym zawodnikiem odnotowanym w kronikach SVB był Gotfryd Gremlowski , wybitny pływak, późniejszy olimpijczyk z Helsinek,19-krotny rekordzista Polski na dystansach od 400 do 1500 metrów, również waterpolista i trener.
„Polonia” Bytom zdobywała Mistrzostwo Polski seniorów w 1947,1949,1959 i 1964 oraz była wicemistrzem kraju w 1948,1957,1958,1960,1962 i 1963.
Do najważniejszych zadań WTS Bytom należy planowanie i organizacja życia sportowego wśród dzieci i młodzieży bytomskich szkół w zakresie piłki wodnej. We współpracy z Miejskim Ośrodkiem Sportu Młodzieżowego w Bytomiu prowadzimy zajęcia treningowe z piłki wodnej na pływalni krytej OSiR w Bytomiu
w czterech grupach szkoleniowych:
- nauki pływania
- szkolenia początkowego
- młodzików
- juniorów i juniorów starszych.
Razem z amatorską drużyną seniorów, piłkę wodną w naszym klubie trenuje blisko 100 zawodników.</p>
